#include <stdio.h>

void  ft_strlen(int *x, int *c)
{
    *x = *c;
    printf("1 - x = %d\tc = %d\n", *x, *c);
}

int main()
{
    int x = 5;
    int c = 1;
    ft_strlen(&x, &c);
    printf("2 - x = %d\tc = %d\n", x, c);
}